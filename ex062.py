# Lista com o nome de 5 pessoas
lista = ["Igor", "Alberto", "Roberto", "Chucky", "Dudu"]

# Exibe o primeiro nome
print(f"Primeiro nome: {lista[0]}")

# Exibe o último nome
print(f"Último nome: {lista[-1]}")

# Exibe todos os nomes, um por linha
print("Nomes na lista:")
for nome in lista:
    print(nome)

# Exibe a quantidade total de elementos
print(f"Total de nomes: {len(lista)}")